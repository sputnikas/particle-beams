#pragma once

#include <vector>

#include "vec3.h"

//////////////////////////////////////////////////////////////////////////////
// Space3d - abstract
//////////////////////////////////////////////////////////////////////////////

class Space3d {
public:
    virtual ~Space3d();

    virtual int include(const Vec3<double> &r) = 0;
};

//////////////////////////////////////////////////////////////////////////////
// Space3dHalf - полупространство
//////////////////////////////////////////////////////////////////////////////

class Space3dHalf : public Space3d {
public:
    Vec3<double> normal;
    Vec3<double> point;

    Space3dHalf();
    Space3dHalf(Vec3<double> normal);
    Space3dHalf(Vec3<double> normal, Vec3<double> point);

    int include(const Vec3<double> &r);
};

//////////////////////////////////////////////////////////////////////////////
// Общие функции
//////////////////////////////////////////////////////////////////////////////

int in_spaces(const std::vector<Space3d*> &spaces, const Vec3<double> r);